export default {
  data() {
    return {
      name: '',
      email: '',
      message: '',
      errors: {
        name: '',
        email: '',
        message: '',
      },
    }
  },

  methods: {
    handleSubmit() {
      // Reset errors
      this.errors.name = ''
      this.errors.email = ''
      this.errors.message = ''

      // Validate inputs
      let isValid = true

      if (!this.name) {
        this.errors.name = 'This input is required'
        isValid = false
      }

      if (!this.email) {
        this.errors.email = 'This input is required'
        isValid = false
      }

      if (!this.message) {
        this.errors.message = 'This input is required'
        isValid = false
      }

      if (isValid) {
        // Handle successful submission (e.g., send data to server)
        console.log('Form submitted:', {
          name: this.name,
          email: this.email,
          message: this.message,
        })
      }
    },
  },
}
document.addEventListener('DOMContentLoaded', function () {
  const firstImage = document.querySelector('.first-image')
  const secondImage = document.querySelector('.second-image')

  const options = {
    root: null,
    rootMargin: '0px',
    threshold: 0.1,
  }

  const callback = (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        // Animate first image to new position
        firstImage.style.transition = 'left 0.5s ease'
        firstImage.style.left = '38%'

        // Animate second image to new position
        secondImage.style.transition = 'left 0.5s ease'
        secondImage.style.left = '40%'
      } else {
        // Reset positions when scrolling away
        firstImage.style.transition = 'left 0.5s ease'
        firstImage.style.left = '20%' // Original position

        secondImage.style.transition = 'left 0.5s ease'
        secondImage.style.left = '50%' // Original position
      }
    })
  }

  const observer = new IntersectionObserver(callback, options)
  observer.observe(document.querySelector('.Phone'))
})
</script>
