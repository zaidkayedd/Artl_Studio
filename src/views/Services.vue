<script>
import { ref, onMounted, onBeforeUnmount } from 'vue'
export default {
  data() {
    return {
      name: '',
      email: '',
      message: '',
      errors: {
        name: '',
        email: '',
        message: '',
      },
    }
  },

  methods: {
    handleSubmit() {
      // Reset errors
      this.errors.name = ''
      this.errors.email = ''
      this.errors.message = ''

      // Validate inputs
      let isValid = true

      if (!this.name) {
        this.errors.name = 'This input is required'
        isValid = false
      }

      if (!this.email) {
        this.errors.email = 'This input is required'
        isValid = false
      }

      if (!this.message) {
        this.errors.message = 'This input is required'
        isValid = false
      }

      if (isValid) {
        // Handle successful submission (e.g., send data to server)
        console.log('Form submitted:', {
          name: this.name,
          email: this.email,
          message: this.message,
        })
      }
    },
    scaleImages() {
      const phoneDiv = document.querySelector('.Phone')
      const images = document.querySelectorAll('.phone-image')

      if (!phoneDiv) return

      const rect = phoneDiv.getBoundingClientRect()
      const windowHeight = window.innerHeight

      // Check if the phone div is in the viewport
      if (rect.top < windowHeight && rect.bottom > 0) {
        // Calculate the scale factor based on the div's position in the viewport
        const centerOfDiv = rect.top + rect.height / 2
        const distanceToCenter = Math.abs(windowHeight / 2 - centerOfDiv)
        let scale = 1 + (1 - distanceToCenter / (windowHeight / 2)) * 0.5 // Adjust the 0.5 to control the scaling speed

        // Apply a maximum scale
        scale = Math.min(scale, 1.5)

        images.forEach((img) => {
          img.style.transform = `scale(${scale})`
        })
      } else {
        // Reset scale if not in viewport
        images.forEach((img) => {
          img.style.transform = 'scale(1)'
        })
      }
    },
  },
  setup() {
    const scrollPosition = ref(0)

    const handleScroll = () => {
      scrollPosition.value = window.scrollY
    }

    onMounted(() => {
      window.addEventListener('scroll', handleScroll)
    })

    onBeforeUnmount(() => {
      window.removeEventListener('scroll', handleScroll)
    })

    return {
      scrollPosition,
    }
  },
  updated() {
    this.scaleImages()
  },
}
</script>
